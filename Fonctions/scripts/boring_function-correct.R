# Vous vous appreztez à écrire votre propre fonction! Tout comme vous effectueriez l'affectation d'une valeur à une variable avec l'opérateur '=', vous assignez une fonction d'une façon un peu similaire:

# function_name = function(arg1, arg2){
#	# Manipuler ici les arguments pour faire ce qu'est censé faire la fonction
# #	Retourner une valeur.
# }
#
# Le nom de la "variable" que vous avez assigné (avant le '=' deviendra ainsi le nom de votre nouvelle fonction.
# arg1 et arg2 représentent les arguments de votre fonction et vous pouvez ainsi les manipuler dans le corps de votre fonction.
# Après avoir validé votre fonction, vous l'utiliserez comme tout autre fonction, en tapant

# function_name(value1, value2)

# ci dessous, nous allons créer une fonction appelée boring_function.
# Cette fonction prend un argument, 'x', en entrée et retourne la valeur de x sans la modifier.

# Pour cette fois-ci, vous n'avez qu'à supprimer le dièse devant le x pour faire fonctionner la fonction!

#Assurez-vous de sauvegarder votre fonction avant de taper submit() dans la console après avoir apporté les changements.

